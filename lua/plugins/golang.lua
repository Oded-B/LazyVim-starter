        function()
          require("dap").terminate()
        end,
        desc = "Terminate",
      },
      {
        "<leader>dw",
        function()
          require("dap.ui.widgets").hover()
        end,
        desc = "Widgets",
      },
      {
        "<leader>dT",
        function()
          require("dap-go").debug_test()
        end,
        desc = "Debug test near cursor",
